namespace c_sharp.reyhane_core.Loop_Exercises._01_while_statements;

public class EasyWhile
{
    //The program that prints numbers from 1 to 5.
    public static void While()
    {
        int i = 1;
        while (i <= 5)
        {
            Console.WriteLine(i);
            i++;
        }
            
    }
}